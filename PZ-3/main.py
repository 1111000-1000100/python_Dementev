A = int(input("Введите первое число: "))
B = int(input("Введите второе число: "))

# Проверяем, что ровно одно из чисел нечетное
is_exactly_one_odd = (A % 2 != 0 and B % 2 == 0) or (A % 2 == 0 and B % 2 != 0)

# Выводим результат
if is_exactly_one_odd:
  print("Ровно одно из чисел A и B нечетное.")
else:
  print("Оба числа A и B четные или оба нечетные.")
